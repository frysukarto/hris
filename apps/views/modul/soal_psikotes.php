                    function add_person()
                    {
                        save_method = 'add';
                        $('#form')[0].reset();
                        $('.form-group').removeClass('has-error');
                        $('.help-block').empty();
                        $('#modal_form').modal('show');
                        $('.modal-title').text('Tambah Soal Psikotes');
                    }

                    function edit_person(id_modul_soal_psikotes)
                    {
                        save_method = 'update';
                        $('#form')[0].reset();
                        $('.form-group').removeClass('has-error');
                        $('.help-block').empty();
                        $.ajax({
                            url: "<?php echo site_url('modul/psikotes/soal_ajax_edit') ?>/" + id_modul_soal_psikotes,
                            type: "GET",
                            dataType: "JSON",
                            success: function (data)
                            {
                                $('[name="id_modul_soal_psikotes"]').val(data.id_modul_soal_psikotes);
                                $('[name="soal_psikotes"]').val(data.soal_psikotes);
                                $('[name="pilihan_A"]').val(data.pilihan_A);
                                $('[name="pilihan_B"]').val(data.pilihan_B);
                                $('[name="pilihan_C"]').val(data.pilihan_C);
                                $('[name="pilihan_D"]').val(data.pilihan_D);
                                $('[name="jawaban"]').val(data.jawaban);
                                $('#modal_form').modal('show');
                                $('.modal-title').text('Edit Soal Psikotes');
                            },
                            error: function (jqXHR, textStatus, errorThrown)
                            {
                                alert('Error get data from ajax');
                            }
                        });
                    }

                    function reload_table()
                    {
                        table.ajax.reload(null, false);
                    }

                    function save()
                    {
                        $('#btnSave').text('saving...');
                        $('#btnSave').attr('disabled', true);
                        var url;

                        if (save_method == 'add') {
                            url = "<?php echo site_url('modul/psikotes/soal_ajax_add') ?>";
                        } else {
                            url = "<?php echo site_url('modul/psikotes/soal_ajax_update') ?>";
                        }

                        // ajax adding data to database
                        $.ajax({
                            url: url,
                            type: "POST",
                            data: $('#form').serialize(),
                            dataType: "JSON",
                            success: function (data)
                            {
                                if (data.status)
                                {
                                    $('#modal_form').modal('hide');
                                    location.reload();
                                } else
                                {
                                    for (var i = 0; i < data.inputerror.length; i++)
                                    {
                                        $('[name="' + data.inputerror[i] + '"]').parent().parent().addClass('has-error');
                                        $('[name="' + data.inputerror[i] + '"]').next().text(data.error_string[i]);
                                    }
                                }
                                $('#btnSave').text('save');
                                $('#btnSave').attr('disabled', false);
                            },
                            error: function (jqXHR, textStatus, errorThrown)
                            {
                                alert('Error adding / update data');
                                $('#btnSave').text('save');
                                $('#btnSave').attr('disabled', false);
                            }
                        });
                    }

                    function delete_person(id_modul_soal_psikotes)
                    {
                        if (confirm('Are you sure delete this data?'))
                        {
                            // ajax delete data to database
                            $.ajax({
                                url: "<?php echo site_url('modul/psikotes/soal_ajax_delete') ?>/" + id_modul_soal_psikotes,
                                type: "POST",
                                dataType: "JSON",
                                success: function (data)
                                {
                                    //if success reload ajax table
                                    $('#modal_form').modal('hide');
                                    location.reload();
                                },
                                error: function (jqXHR, textStatus, errorThrown)
                                {
                                    alert('Error deleting data');
                                }
                            });

                        }
                    }
